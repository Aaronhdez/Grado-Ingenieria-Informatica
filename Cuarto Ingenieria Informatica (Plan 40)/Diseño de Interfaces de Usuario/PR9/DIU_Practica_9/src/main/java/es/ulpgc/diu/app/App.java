    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        String user = this.usernameField.getText();
        String password = String.copyValueOf(this.passwordField.getPassword());
        if(checkData(user, password)){
            try {
                this.connection = this.sqlHelper.conectToLocalFileDatabase(user, password);
                //this.connection = this.sqlHelper.conectToLocalDatabase(user, password);
                //this.connection = this.sqlHelper.conectToRemoteDatabase(user, password);
                JOptionPane.showMessageDialog(loginDialog, "Conexión realizada con éxito", "Conexión realizada", JOptionPane.INFORMATION_MESSAGE);
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(App.class.getName()).log(Level.SEVERE, null, ex);
            } catch (FileNotFoundException ex) {
                Logger.getLogger(App.class.getName()).log(Level.SEVERE, null, ex);
            }
            setConnection(user);
        }
    }//GEN-LAST:event_loginButtonActionPerformed

    private void setConnection(String user) {
        if(this.connection != null){
            this.userMenu.setText(user);
            this.QueryDB();
            this.loginDialog.dispose();
        }
    }

    private boolean checkData(String user, String password) {
        if(user.equals("")){
            JOptionPane.showMessageDialog(loginDialog, "El nombre de usuario no puede estar vacío", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(password.equals("")){
            JOptionPane.showMessageDialog(loginDialog, "La contraseña no puede estar vacía", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }
    
