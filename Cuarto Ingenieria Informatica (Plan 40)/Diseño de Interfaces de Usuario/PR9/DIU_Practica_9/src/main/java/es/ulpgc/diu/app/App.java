    private void queryResultListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_queryResultListMouseClicked
        List<String> selectedRows = this.queryResultList.getSelectedValuesList();
        String tableFieldsList = "";
        for (String tableName : selectedRows) {
            ArrayList<Object> tableFields= SQLHelper.queryTablesData(this.connection, tableName);
            for (Object tableField : tableFields) {
                tableFieldsList += tableName + "." + (String) tableField + "\n";
            }
            tableFieldsList += "\n";
        }
        this.tableFieldsTextArea.setText(tableFieldsList);
    }//GEN-LAST:event_queryResultListMouseClicked

