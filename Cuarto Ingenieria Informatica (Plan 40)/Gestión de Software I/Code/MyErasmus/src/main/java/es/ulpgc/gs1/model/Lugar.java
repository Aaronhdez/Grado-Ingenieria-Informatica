package es.ulpgc.gs1.model;

/**
 *
 * @author Andres
 */
public class Lugar {
    private String street;
    private String city;

    public Lugar(String street, String city) {
        this.street = street;
        this.city = city;
    }

    public String getStreet() {
        return street;
    }

    public String getCity() {
        return city;
    }

    public void setStreet(String street) {
        this.street = street;
    }

    public void setCity(String city) {
        this.city = city;
    }
}
